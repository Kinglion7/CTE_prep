/**
 * Reads LIST-DATA from public/DCIPS_Template.xlsx and writes
 * src/lib/dcipsPicklists.generated.ts
 */
import XLSX from "xlsx";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const templatePath = path.join(root, "public", "DCIPS_Template.xlsx");
const outPath = path.join(root, "src", "lib", "dcipsPicklists.generated.ts");

const wb = XLSX.read(fs.readFileSync(templatePath));
const ws = wb.Sheets["LIST-DATA"];
if (!ws || !ws["!ref"]) {
  throw new Error("LIST-DATA sheet missing");
}
const range = XLSX.utils.decode_range(ws["!ref"]);

/** Data rows start after header row 4 (0-based index 4); first value row is 5. */
const FIRST_DATA_ROW = 5;

function uniqueColumn(colIndex) {
  const s = new Set();
  for (let r = FIRST_DATA_ROW; r <= range.e.r; r++) {
    const addr = XLSX.utils.encode_cell({ r, c: colIndex });
    const cell = ws[addr];
    if (cell?.v != null) {
      const t = String(cell.v).trim();
      if (t) s.add(t);
    }
  }
  return [...s].sort((a, b) => a.localeCompare(b));
}

/** Column indices match LIST-DATA row 5 in DCIPS_Template.xlsx */
const COLUMN_BY_FIELD = {
  Casualty_Type_Status_Category: 0,
  Inflicting_Force: 2,
  Field_Report_Type: 4,
  Personnel_Type: 6,
  Duty_Status: 9,
  Country: 11,
  State: 15,
  Branch_of_Service: 25,
  Rank: 28,
  Operation: 32,
};

let ts =
  "/** Auto-generated by scripts/extract-dcips-lists.mjs from public/DCIPS_Template.xlsx LIST-DATA. */\n\n";
ts += "import type { DcipsExtracted } from \"../types/dcips\";\n\n";
ts += "export const DCIPS_DROPDOWN_KEYS = [\n";
for (const k of Object.keys(COLUMN_BY_FIELD)) {
  ts += `  \"${k}\",\n`;
}
ts += `  \"Gender\",\n`;
ts += "] as const;\n\n";
ts += "export type DcipsDropdownKey = (typeof DCIPS_DROPDOWN_KEYS)[number];\n\n";
ts += "export const DCIPS_FIELD_OPTIONS: Record<\n";
ts += "  DcipsDropdownKey,\n";
ts += "  readonly string[]\n";
ts += "> = {\n";

for (const [field, col] of Object.entries(COLUMN_BY_FIELD)) {
  const arr = uniqueColumn(col);
  ts += `  ${field}: ${JSON.stringify(arr)},\n`;
}
ts += `  Gender: ["Male", "Female"],\n`;
ts += "};\n\n";
ts +=
  "export function optionsForField(key: keyof DcipsExtracted): readonly string[] | undefined {\n";
ts +=
  "  if (!Object.prototype.hasOwnProperty.call(DCIPS_FIELD_OPTIONS, key)) return undefined;\n";
ts += "  return DCIPS_FIELD_OPTIONS[key as DcipsDropdownKey];\n";
ts += "}\n";

fs.writeFileSync(outPath, ts, "utf8");
console.log("Wrote", outPath);
